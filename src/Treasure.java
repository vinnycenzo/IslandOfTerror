public class Treasure {
    private String name;
    public int amt;

    //constructor
    public Treasure(String name, int amt) {
        this.name = name;
        this.amt = amt;
    }

    public int getAmt() {
        return amt;
    }
    public int totalAmt() {
        return amt;
    }

    public String getName() {
        return name;
    }
}

